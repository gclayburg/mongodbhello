package com.garyclayburg.scimclient;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.util.List;

/**
 * Created by IntelliJ IDEA.
 * Date: 2/2/15
 * Time: 12:54 PM
 *
 * @author Gary Clayburg
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class SCIMusersDoc {
    @SuppressWarnings("UnusedDeclaration")
    private static final Logger log = LoggerFactory.getLogger(SCIMusersDoc.class);

    private int totalResults;
    private int itemsPerPage;
    private int startIndex;
    private List<String> schemas;

    @JsonProperty(value = "Resources")  //Jackson mapping by default won't match capitalized Resources
    private List<com.garyclayburg.scimclient.Resources> resources;

    public int getTotalResults() {
        return totalResults;
    }

    public void setTotalResults(int totalResults) {
        this.totalResults = totalResults;
    }

    public int getItemsPerPage() {
        return itemsPerPage;
    }

    public void setItemsPerPage(int itemsPerPage) {
        this.itemsPerPage = itemsPerPage;
    }

    public int getStartIndex() {
        return startIndex;
    }

    public void setStartIndex(int startIndex) {
        this.startIndex = startIndex;
    }

    public List<String> getSchemas() {
        return schemas;
    }

    public void setSchemas(List<String> schemas) {
        this.schemas = schemas;
    }

    public List<Resources> getResources() {
        return resources;
    }

    public void setResources(List<Resources> resources) {
        this.resources = resources;
    }
}
